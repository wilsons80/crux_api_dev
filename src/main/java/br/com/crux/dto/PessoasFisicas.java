package br.com.crux.dto;

public class PessoasFisicas {

	private String nmPessoaFisica;
	private long nrCpf;
	
	
	public PessoasFisicas() {
	}


	public String getNmPessoaFisica() {
		return nmPessoaFisica;
	}


	public void setNmPessoaFisica(String nmPessoaFisica) {
		this.nmPessoaFisica = nmPessoaFisica;
	}


	public long getNrCpf() {
		return nrCpf;
	}


	public void setNrCpf(long nrCpf) {
		this.nrCpf = nrCpf;
	}

	
}
